#include "shell_hd.h"

/**
 * main - entry point
 * @ac: arg count
 * @av: arg vector
 *
 * Return: 0 on success, 1 on error
 */
int main(int ac, char **av)
{
	info_t _Info_[] = { INFO_INIT };
	int fd = 2;

	asm ("mov %1, %0\n\t"
		"add $3, %0"
		: "=r" (fd)
		: "r" (fd));

	if (ac == 2)
	{
		fd = open(av[1], O_RDONLY);
		if (fd == -1)
		{
			if (errno == EACCES)
				exit(126);
			if (errno == ENOENT)
			{
				_eputs(av[0]);
				_eputs(": 0: Can't open ");
				_eputs(av[1]);
				_eputchar('\n');
				_eputchar(FLUSH_BUF);
				exit(127);
			}
			return (EXIT_FAILURE);
		}
		_Info_->read_fd = fd;
	}
	populate_env_list(_Info_);
	read_history(_Info_);
	hsh(_Info_, av);
	return (EXIT_SUCCESS);
}
